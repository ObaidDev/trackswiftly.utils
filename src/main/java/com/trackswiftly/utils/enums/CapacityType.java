package com.trackswiftly.utils.enums;



/**
 * Enum representing different types of capacity measurements.
 */
public enum CapacityType {

        /** Capacity measured by weight. */
        WEIGHT,


        /** Capacity measured by volume. */
        VOLUME,

        /** Capacity measured by number of boxes. */
        BOX_COUNT,


        /** Capacity measured by number of pallets. */
        PALLETS,


        /** Custom capacity measurement type. */
        CUSTOM
}
